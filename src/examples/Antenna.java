    private void btnGenerarReqActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGenerarReqActionPerformed
        try {
            if(cxi.getEstatus())
            {
                String llave="";
                String grupo="";
                String[] resultado =cmbLlave.getSelectedItem().toString().split("-");
                llave=resultado[0];
                grupo= resultado[1];

                String modulus = cxi.ObtenerModulus(llave,grupo);
                if(! modulus.equals(""))
                {
                    modulus= modulus.toUpperCase();
                    Req requerimiento = new Req();

                    String mensaje = requerimiento.GenerarReq(
                        modulus,
                        txtRfc.getText(),
                        txtNoOficio.getText(),
                        llave,
                        grupo,
                        txtRutaSalida.getText(),
                        cxi);
                    if(! mensaje.equals(""))
                    JOptionPane.showMessageDialog(null,mensaje);
                    else
                    JOptionPane.showMessageDialog(null,"El requerimiento se ha creado de forma correcta.");
                }
                else
                JOptionPane.showMessageDialog(null,"No se ha podido encontrar la llave");

            }
            else
            {
                JOptionPane.showMessageDialog(null,"No se encuentra conectado al dispositivo");
            }

        } catch (Exception ex) {
            Logger.getLogger(Antenna.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnGenerarReqActionPerformed
    
