    private void btnGenerarLlavebtnConectarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGenerarLlavebtnConectarActionPerformed
        try {
            String mensaje = cxi.GenerarLlave(txtNombreLlave.getText(),txtGrupo.getText());
            if(! mensaje.equals(""))
                JOptionPane.showMessageDialog(null,mensaje);
            else
            {
                JOptionPane.showMessageDialog(null,"La llave se gener√≥ correctamente");
                KeyAttributes[] llaves= cxi.getLista();
                if(llaves != null)
                {
                    cmbLlave.removeAllItems();
                    for(int i=0;i< llaves.length;i++)
                    {
                        cmbLlave.addItem(llaves[i].getName() + "-" +llaves[i].getGroup());
                    }
                }
//                txtNombreLlaveReq.setText(txtNombreLlave.getText());
//                txtGrupoReq.setText(txtGrupo.getText());
            }
            
        } catch(Exception ee) {
        }
}//GEN-LAST:event_btnGenerarLlavebtnConectarActionPerformed

    private void btnConectarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConectarActionPerformed
        try {
            cxi = new CXI();
            String mensaje =
                    cxi.Conectar(
                    txtUsuario.getText(),
                    txtPassword.getText(),
                    txtIp.getText()
                    );
            if(mensaje.equals("")) {
                panelEstatus.setBackground(Color.GREEN);
                lblEstado.setText("CONECTADO");
                KeyAttributes[] llaves= cxi.getLista();
                if(llaves != null)
                {
                    cmbLlave.removeAllItems();
                    for(int i=0;i< llaves.length;i++)
                    {
                        cmbLlave.addItem(llaves[i].getName() + "-" +llaves[i].getGroup());
                    }
                }
                
            } else {
                JOptionPane.showMessageDialog(null,mensaje);
            }


        } catch (NumberFormatException ex) {
            Logger.getLogger(Antenna.class.getName()).log(Level.SEVERE, null, ex);
        } catch (CryptoServerException ex) {
            Logger.getLogger(Antenna.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnConectarActionPerformed

